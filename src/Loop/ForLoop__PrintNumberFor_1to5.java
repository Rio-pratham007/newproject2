package Loop;

public class ForLoop__PrintNumberFor_1to5 
{
	public static void main(String[] args)
	{
		//start no : 1
		//end no :5
		//incre or decr: incre by 1
	
		for(int i=1; i<=5; i++)           // 1<=10   = 1
		{                                 // 2<=10   = 2
			System.out.println(i);        // 3<=10   = 3
		}                                 // 4<=10   = 5
		
	}

}
